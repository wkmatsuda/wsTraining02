--ユーザマスタ
CREATE TABLE M_USER(
	USER_ID	VARCHAR(8) NOT NULL PRIMARY KEY,
	PASSWORD	VARCHAR(8) NOT NULL,
	USER_NAME_KANJI	VARCHAR(60) NOT NULL,
	USER_NAME_KANA	VARCHAR(60),
	ADMIN_FLG	VARCHAR(2) NOT NULL,
	DEPARTMENT_KBN	VARCHAR(2) NOT NULL,
	POSITION_KBN	VARCHAR(2) NOT NULL,
	REG_DT TIMESTAMP DEFAULT now(),
	REG_USER_ID	VARCHAR(8),
	UPD_DT TIMESTAMP DEFAULT now(),
	UPD_USER_ID	VARCHAR(8),
	UPD_SRESNO	BIGINT DEFAULT 1,
	DEL_FLG	VARCHAR(2) DEFAULT '01'
)

--部署マスタ
CREATE TABLE M_DEPARTMENT(
	DEPARTMENT_KBN	VARCHAR(2) NOT NULL PRIMARY KEY,
	DEPARTMENT_NAME	VARCHAR(40) NOT NULL,
	SORT_NO	BIGINT NOT NULL,
	REG_DT TIMESTAMP DEFAULT now(),
	REG_USER_ID	VARCHAR(8),
	UPD_DT TIMESTAMP DEFAULT now(),
	UPD_USER_ID	VARCHAR(8),
	UPD_SRESNO	BIGINT DEFAULT 1,
	DEL_FLG	VARCHAR(2) DEFAULT '01'
)

--役職マスタ
CREATE TABLE M_POSITION(
	POSITION_KBN	VARCHAR(2) NOT NULL PRIMARY KEY,
	POSITION_NAME	VARCHAR(40) NOT NULL,
	SORT_NO	BIGINT NOT NULL,
	REG_DT TIMESTAMP DEFAULT now(),
	REG_USER_ID	VARCHAR(8),
	UPD_DT TIMESTAMP DEFAULT now(),
	UPD_USER_ID	VARCHAR(8),
	UPD_SRESNO	BIGINT DEFAULT 1,
	DEL_FLG	VARCHAR(2) DEFAULT '01'
)

--勤怠トラン
CREATE TABLE T_ATTENDANCE(
	USER_ID	VARCHAR(8) NOT NULL,
	WORK_DT	DATE NOT NULL,
	CLOCK_IN_DT TIMESTAMP,
	CLOCK_OUT_DT TIMESTAMP,
	REG_DT TIMESTAMP DEFAULT now(),
	REG_USER_ID	VARCHAR(8),
	UPD_DT TIMESTAMP DEFAULT now(),
	UPD_USER_ID	VARCHAR(8),
	UPD_SRESNO	BIGINT DEFAULT 1,
	DEL_FLG	VARCHAR(2) DEFAULT '01',
	PRIMARY KEY(USER_ID,WORK_DT)
)

